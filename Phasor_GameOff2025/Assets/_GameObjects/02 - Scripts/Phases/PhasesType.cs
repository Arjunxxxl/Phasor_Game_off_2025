public enum PhasesType
{
    Unknown = -1,
    Default,
    TimeShift, // Stops The Time
    Air, // Can jump higher
    AstralPlane, // Walk over light beams
    Shadow, // Can escape the obstacles but can interact with anything
    Ember, // immune to fire
    Frost, // Freeze the water underneath
    Mirror, // Mirror image is generated in front of player - both moves together
}
